            <section class="legal-section">
                <h3>1. Introduction</h3>
                <p>
                    La protection de vos données personnelles est au cœur de nos préoccupations. L'Université de Limoges, en tant que responsable de traitement, s'engage à ce que la collecte et le traitement de vos données, effectués à partir du site <strong>Questionary</strong>, soient conformes au Règlement Général sur la Protection des Données (RGPD) et à la loi Informatique et Libertés.
                </p>
            </section>

            <section class="legal-section">
                <h3>2. Données collectées</h3>
                <p>Nous limitons la collecte des données personnelles au strict nécessaire (minimisation des données). Les données collectées sont les suivantes :</p>
                <ul class="legal-list">
                    <li><strong>Données d'identité :</strong> Lors de votre connexion via le CAS (Central Authentication Service), nous recevons votre identifiant (login), votre nom, votre prénom et votre adresse e-mail institutionnelle.</li>
                    <li><strong>Données de connexion :</strong> Adresse IP, type de navigateur, horodatage des connexions (logs serveurs) pour des raisons de sécurité.</li>
                    <li><strong>Données relatives aux questionnaires :</strong> Contenus créés, réponses apportées (qui peuvent être anonymes ou nominatives selon la configuration choisie par le créateur du questionnaire).</li>
                </ul>
            </section>

            <section class="legal-section">
                <h3>3. Finalités du traitement</h3>
                <p>Les données sont traitées pour les finalités suivantes :</p>
                <ul class="legal-list">
                    <li>Gestion de votre compte utilisateur et authentification sécurisée.</li>
                    <li>Fonctionnement technique de la plateforme (création, diffusion et analyse des questionnaires).</li>
                    <li>Assistance utilisateur et maintenance.</li>
                    <li>Statistiques anonymes d'utilisation du service pour en améliorer la qualité.</li>
                </ul>
            </section>

            <section class="legal-section">
                <h3>4. Durée de conservation</h3>
                <p>
                    Vos données sont conservées pendant la durée nécessaire à l'accomplissement des finalités mentionnées ci-dessus.
                </p>
                <ul class="legal-list">
                    <li><strong>Compte inactif :</strong> Les données de compte sont supprimées après 2 ans d'inactivité.</li>
                    <li><strong>Journaux de connexion :</strong> Conservés légalement pendant 1 an.</li>
                    <li><strong>Questionnaires et réponses :</strong> Conservés sous la responsabilité du créateur jusqu'à leur suppression manuelle ou la suppression du compte.</li>
                </ul>
            </section>

            <section class="legal-section">
                <h3>5. Sécurité des données</h3>
                <p>
                    Nous mettons en œuvre des mesures techniques et organisationnelles appropriées pour protéger vos données contre l'accès non autorisé, la divulgation, l'altération ou la destruction (chiffrement TLS, pare-feu, gestion stricte des accès serveurs).
                </p>
            </section>

            <section class="legal-section">
                <h3>6. Vos droits</h3>
                <p>Conformément à la réglementation, vous disposez des droits suivants sur vos données :</p>
                <ul class="legal-list">
                    <li>Droit d'accès et de rectification.</li>
                    <li>Droit à l'effacement ("droit à l'oubli").</li>
                    <li>Droit à la limitation du traitement.</li>
                    <li>Droit d'opposition.</li>
                </ul>
                <p>Pour exercer ces droits, vous pouvez contacter notre Délégué à la Protection des Données (DPO) à l'adresse suivante : <strong>dpo@unilim.fr</strong>.</p>
                <p>Si vous estimez, après nous avoir contactés, que vos droits ne sont pas respectés, vous pouvez adresser une réclamation à la CNIL.</p>
            </section>

