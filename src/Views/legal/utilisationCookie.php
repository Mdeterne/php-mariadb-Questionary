            <h1 class="section-title">Utilisation des Cookies</h1>

            <p>
                Cette politique explique comment <strong>Questionary</strong> (plateforme de l'Université de Limoges) utilise des cookies et technologies similaires pour vous fournir un service sécurisé et fonctionnel.
            </p>

            <h3>1. Qu'est-ce qu'un cookie ?</h3>
            <p>
                Un cookie est un petit fichier texte enregistré sur votre terminal (ordinateur, tablette ou smartphone) lors de la visite d'un site. Il permet au site de mémoriser vos actions et préférences sur une période donnée.
            </p>

            <h3>2. Cookies strictement nécessaires</h3>
            <p>
                Nous utilisons uniquement des cookies indispensables au bon fonctionnement de l'application. Sans ces cookies, vous ne pourriez pas vous connecter ou utiliser les services de base.
            </p>
            <ul style="margin-bottom: 16px; padding-left: 20px; color: var(--muted); line-height: 1.6;">
                <li><strong>PHPSESSID :</strong> Ce cookie est généré nativement par PHP. Il est essentiel pour maintenir votre session active une fois connecté via le portail universitaire. Il expire généralement à la fermeture de votre navigateur.</li>
                <li><strong>Cookies d'authentification (CAS) :</strong> Des cookies tiers peuvent être déposés par le Service Central d'Authentification (CAS) de l'université pour gérer votre connexion sécurisée (Single Sign-On).</li>
            </ul>

            <h3>3. Absence de cookies publicitaires</h3>
            <p>
                <strong>Questionary</strong> est un outil académique et pédagogique. Nous n'utilisons <strong>aucun cookie publicitaire</strong>, ni aucun traceur commercial ou de réseaux sociaux destiné à revendre vos données de navigation.
            </p>

            <h3>4. Gestion des cookies</h3>
            <p>
                La plupart des navigateurs acceptent les cookies par défaut. Vous pouvez configurer votre navigateur pour refuser les cookies, mais attention : bloquer les cookies de session (PHPSESSID) vous empêchera de vous connecter à l'application.
            </p>

